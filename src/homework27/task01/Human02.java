package homework27.task01;

public class Human02 {

  private String name;
  private int age;


  public Human02(String name, int age) {
    this.name = name;
    this.age = age;
  }


  public String getName() {
    return name;
  }
  public void setName(String name){
    this.name = name;
  }
  public void setAge(int age){
    this.age = age;
  }
  public int getAge(){
    return age;
  }

}
